#pragma once

#include "vec2.h"

// ----------------------------------------------------------------------------
// Топологические поверхности, получаемые склейкой краёв квадрата
// (фундаментального многоугольника). Игровое поле W x H — это и есть квадрат,
// а правила выхода за край определяются тем, как склеены его стороны:
//
//   Plane       — края не склеены (стены).               a, b, a', b' свободны
//   Cylinder    — склеены лево-право, верх/низ — стены.   a b a^{-1}  (только)
//   Torus       — лево-право и верх-низ напрямую.         a b a^{-1} b^{-1}
//   Mobius      — лево-право с переворотом, верх/низ стены.
//   Klein       — лево-право с переворотом, верх-низ напрямую.  a b a b^{-1}
//   Projective  — оба края с переворотом (RP^2).          a b a b
// ----------------------------------------------------------------------------
enum class Topology {
    Plane,
    Cylinder,
    Torus,
    Mobius,
    Klein,
    Projective
};

// Сколько всего поверхностей (для переключения клавишами 1..N).
constexpr int kTopologyCount = 6;

// Человекочитаемое имя поверхности (для интерфейса).
const char* topologyName(Topology t);

// Результат пересечения края поля.
struct Transition {
    Vec2 pos;     // скорректированная позиция головы
    Vec2 dir;     // скорректированное направление движения
    bool alive;   // false, если врезались в стену
};

// Применяет правила склейки для заданной топологии.
//
// cand — позиция-кандидат после шага (может выходить за границу ровно на одну
//        клетку, т.к. змейка ходит по осям единичными шагами);
// dir  — текущее направление; W, H — размеры поля.
//
// Если cand внутри поля — возвращает его без изменений. Иначе применяет
// отображение склейки соответствующего края.
Transition wrap(Topology t, Vec2 cand, Vec2 dir, int W, int H);
